
    class Frequency {

        String word;
        int frequency;

        public Frequency(String w, int f){
            word = w;
            frequency = f;
        }

        public String getWord(){

            return word;
        }

        public int getFrequency(){

            return frequency;
        }

        public void setFrequency(int f){

            frequency = f;
        }
}
